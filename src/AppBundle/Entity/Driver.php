<?php

namespace AppBundle\Entity;

/**
 * Driver
 */
class Driver
{
    /**
     * @var int
     */
    private $id;

    /**
     * @var string
     */
    private $name;

    /**
     * @var int
     */
    private $age;


    /**
     * Get id
     *
     * @return int
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set name
     *
     * @param string $name
     *
     * @return Driver
     */
    public function setName($name)
    {
        $this->name = $name;

        return $this;
    }

    /**
     * Get name
     *
     * @return string
     */
    public function getName()
    {
        return $this->name;
    }

    /**
     * Set age
     *
     * @param integer $age
     *
     * @return Driver
     */
    public function setAge($age)
    {
        $this->age = $age;

        return $this;
    }

    /**
     * Get age
     *
     * @return int
     */
    public function getAge()
    {
        return $this->age;
    }
    /**
     * @var \AppBundle\Entity\Avtomobile
     */
    private $car;


    /**
     * Set car
     *
     * @param \AppBundle\Entity\Avtomobile $car
     *
     * @return Driver
     */
    public function setCar(\AppBundle\Entity\Avtomobile $car = null)
    {
        $this->car = $car;

        return $this;
    }

    /**
     * Get car
     *
     * @return \AppBundle\Entity\Avtomobile
     */
    public function getCar()
    {
        return $this->car;
    }
}
